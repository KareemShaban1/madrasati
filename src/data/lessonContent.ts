export const getLessonContent = (subjectId: string, lessonId: string): LessonContent | null => {
  // Try direct lookup in grade-specific content first
  const directKey = `${subjectId}-${lessonId}`;
  if (grade6Content[directKey as keyof typeof grade6Content]) {
    return grade6Content[directKey as keyof typeof grade6Content];
  }
  
  // Try in main lessonsContent
  if (lessonsContent[directKey]) {
    return lessonsContent[directKey];
  }
  
  // Try mapped subject ID for backwards compatibility
  const subjectContentMapping: Record<string, string> = {
    "p6-math": "mathematics",
    "p1-math": "mathematics",
    "p2-math": "mathematics",
    "p3-math": "mathematics",
    "p4-math": "mathematics",
    "p5-math": "mathematics",
    "prep1-math": "mathematics",
    "prep2-math": "mathematics",
    "prep3-math": "mathematics",
    "sec1-math": "mathematics",
    "sec2-math": "mathematics",
    "sec3-math": "mathematics",
  };
  
  const mappedSubjectId = subjectContentMapping[subjectId];
  if (mappedSubjectId) {
    // Convert lesson-1 format to just "1"
    const numericLessonId = lessonId.replace("lesson-", "");
    const mappedKey = `${mappedSubjectId}-${numericLessonId}`;
    if (lessonsContent[mappedKey]) {
      return lessonsContent[mappedKey];
    }
  }
  
  // Fallback to first available lesson content (for demo purposes)
  return lessonsContent["mathematics-1"] || null;
};
